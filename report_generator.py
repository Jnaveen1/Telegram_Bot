def generate_daily_pdf_report(report_date):

    production_records = get_daily_summary(report_date)

    feed_records = get_all_feeds(report_date)

    medicine_records = get_all_medicines()

    feed_totals = get_feed_totals_kg()

    medicine_totals = get_medicine_totals_kg()

    pending = []

    missing = get_missing_fields(report_date)

    for shed, fields in missing.items():

        pending.append({

            "shed": shed,

            "missing": fields

        })

    report_data = {

        "report_date": report_date,

        "production_records": production_records,

        "feed_records": feed_records,

        "medicine_records": medicine_records,

        "production_totals": {},

        "feed_totals": feed_totals,

        "medicine_totals": medicine_totals,

        "pending_reports": pending,

    }

    return generate_daily_pdf(report_data)

def generate_weekly_pdf_report(period):

    production_records = get_weekly_summary(period)

    today = date.today()

    if period == "last_week":

        this_week_start = today - timedelta(days=today.weekday())

        start_date = this_week_start - timedelta(days=7)

        end_date = this_week_start - timedelta(days=1)

        report_title = "Last Week"

    else:

        start_date = today - timedelta(days=today.weekday())

        end_date = today

        report_title = "This Week"

    # Fetch feed records for the selected week
    feed_records = get_all_feeds(start_date, end_date)

    medicine_records = get_all_medicines()

    feed_totals = get_feed_totals_kg()

    medicine_totals = get_medicine_totals_kg()

    report_data = {

        "report_date": report_title,

        "production_records": production_records,

        "feed_records": feed_records,

        "medicine_records": medicine_records,

        "production_totals": {},

        "feed_totals": feed_totals,

        "medicine_totals": medicine_totals,

        "pending_reports": []

    }

    return generate_weekly_pdf(report_data)

